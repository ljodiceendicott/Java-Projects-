    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        // TODO add your handling code here:
        
        if(merchInv.get(selectionMerch).getPrice()>playerGold){
            JOptionPane.showMessageDialog(null, "The player has insufficient funds to make this purchase");
        }
        else{
            playerGold= playerGold- merchInv.get(selectionMerch).getPrice();
            jtfPlayersGold.setText(Integer.toString(playerGold));
            playerInv.add(merchInv.get(selectionMerch));
            playerdlm.addElement(merchInv.get(selectionMerch).getShort());
            merchInv.remove(merchInv.get(selectionMerch));
            merchdlm.removeElementAt(selectionMerch);
            if(merchInv.isEmpty()){
                btnVendorInfo.setEnabled(false);
            }
              btnBuy.setEnabled(false);
        }
    }//GEN-LAST:event_btnBuyActionPerformed


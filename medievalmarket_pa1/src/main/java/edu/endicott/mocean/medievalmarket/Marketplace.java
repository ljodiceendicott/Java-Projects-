    private void jlstPlayerItemsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jlstPlayerItemsValueChanged
        // this changes so that when a new item is clicked that it gives us the number throught selection
       if(evt.getValueIsAdjusting()){
           selectionPlayer=evt.getLastIndex();
           btnPlayerInfo.setEnabled(true);
           btnSell.setEnabled(true);
       }
    }//GEN-LAST:event_jlstPlayerItemsValueChanged

    private void jlstMerchItemsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jlstMerchItemsValueChanged
        // TODO add your handling code here:
        if(evt.getValueIsAdjusting()){
            selectionMerch= evt.getLastIndex();
            btnVendorInfo.setEnabled(true);
            btnBuy.setEnabled(true);
        }
    }//GEN-LAST:event_jlstMerchItemsValueChanged

